    function Chg_button_view(x){                                     // For events like mouse-over....change colour of button...
		x.style.backgroundColor="#f5f5dc";
		x.style.border="1px solid gray";
		
	}

	function Normal_button_view(x){                                 // For events like mouse-out....change colour of button to normal back again...
		x.style.backgroundColor="#F5F5F5";
		x.style.border="1px solid whitesmoke";
    }